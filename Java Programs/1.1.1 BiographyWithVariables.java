public class BiographyWithVariables {

	public static void main(String[] args) {
    
		String name = "Jane Doe";
		int age = 52;
		String state = "Florida";
		String country = "The United States of America";
		String subject = "Math";
		String videoGame = "Super Smash Brothers for Wii U and 3DS";
		
		System.out.println("My name is " + name + ".");
		System.out.println("I am " + age + " years old.");
		System.out.println("I am from the state of " + state + " in " + country + ".");
		System.out.println("My favorite subject in school is " + subject + ".");
		System.out.println("My favorite video game is " + videoGame + ".");
	}
}
